#include "libft.h"

void	ft_intrev(int *s)
{
	int	temp;
	int	*e;
	int	len;

	len = ft_int_array_len(s);
	e = s + len - 1;
	while (s < e)
	{
		temp = *s;
		*s = *e;
		*e = temp;
		s++;
		e--;
	}
}
